function parseJwt(token) {
  var base64Url = token.split(".")[1];
  var base64 = base64Url.replace(/-/g, "+").replace(/_/g, "/");
  var jsonPayload = decodeURIComponent(
    window
      .atob(base64)
      .split("")
      .map(function (c) {
        return "%" + ("00" + c.charCodeAt(0).toString(16)).slice(-2);
      })
      .join("")
  );

  return JSON.parse(jsonPayload);
}

function getDataArray(data) {
  let newArr = [];
  for (let i = 0; i < data.length; i++) {
    if (
      newArr.filter((f) => f.asset_contract === data[i].asset_contract.address)
        .length === 0
    ) {
      newArr.push({
        asset_contract: data[i].asset_contract.address,
        type: data[i].asset_contract.schema_name,
        data: data.filter(
          (f) => f.asset_contract.address === data[i].asset_contract.address
        ),
      });
    }
  }
  return newArr;
}
